    private void init() {
        this.setLocationRelativeTo(null);
        this.setIconImage(XImage.getAppIcon());
        new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date now = new Date();
                SimpleDateFormat dateFormat = new SimpleDateFormat("hh:mm:ss a");
                String text = dateFormat.format(now);
                lblClock.setText(text);
            }
        }).start();
        this.fillComboBoxThematic();
        this.updateStatus();
    }

    private void first() {
        this.row = 0;
        this.edit();
    }

    private void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    private void next() {
        if (this.row < tblListCourse.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    private void last() {
        this.row = tblListCourse.getRowCount() - 1;
        this.edit();
    }

    void fillComboBoxThematic() {
        
                DefaultComboBoxModel model = (DefaultComboBoxModel) cboThematic.getModel();
        model.removeAllElements();
        List<Thematic> list = tdao.selectAll();
        for (Thematic thematic : list) {
            model.addElement(thematic);
       
  
        }
        

    }

    void fillTabel() {
        DefaultTableModel model = (DefaultTableModel) tblListCourse.getModel();
        model.setRowCount(0);
        try {
            Thematic thematic = (Thematic) cboThematic.getSelectedItem();
            List<Course> list = dao.selectByThematic(thematic.getThematicCode());
            for (Course course : list) {
                Object[] row = {
                    course.getCourseCode(),
                    course.getTime(),
                    course.getTuition(),
                    XDate.toString(course.getKhaigiang(), "mm/dd/yyyy"),
                    course.getStaffCode(),
                    XDate.toString(course.getNgaytaoDate(), "mm/dd/yyyy")
                };
                model.addRow(row);
            }

        } catch (Exception e) {
            MsgBox.alert(this, "thua");
        }
    }
    
    void selectThematic() {
        Thematic thematic = (Thematic) cboThematic.getSelectedItem();
        if (thematic == null) return;
        txtThematic.setText(String.valueOf(thematic.getThematicCode()));
        txtTime.setText(String.valueOf(thematic.getTime()));
        txtTuition.setText(String.valueOf(thematic.getTuition()));
        lblThematic2.setText(thematic.getThematicName());
        txtNote.setText(thematic.getThematicName());
        this.fillTabel();
        this.row = -1;
        this.updateStatus();
        jTabbedPane1.setSelectedIndex(1);
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblListCourse.getRowCount() - 1);
        //trang thai form
//        txtLearnerCode.setEditable(!edit);
        btnAdd.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);
        //trang tthai dieu huong
        txtThematic.setEditable(false);
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

