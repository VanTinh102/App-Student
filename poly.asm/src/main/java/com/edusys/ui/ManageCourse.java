    void insert() {
        Course model = getForm();
        model.setNgaytaoDate(new Date());
        try {
            dao.insert(model);
            this.fillTabel();
            this.clear();
            MsgBox.alert(this, MsgBox.msginsert);
        } catch (Exception e) {
            MsgBox.alert(this, MsgBox.msgnotinsert);

        }

    }

    ;
    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "you do not have the right to delete employees");
        } else {
            Integer id = Integer.valueOf(cboThematic.getToolTipText());
            if (MsgBox.confirm(this, "Do you want to delete this employee?")) {
                try {
                    dao.delete(id);
                    this.fillTabel();
                    this.clear();
                    MsgBox.alert(this, "successful delete");
                } catch (Exception e) {
                    MsgBox.alert(this, "delete failed");
                }
            }
        }
    }

    ;
    void update() {
        Course model = getForm();
        try {
            dao.update(model);
            this.fillTabel();
            MsgBox.alert(this, MsgBox.msgupdate);
        } catch (Exception e) {
            MsgBox.alert(this, MsgBox.msgnotupdate);
        }
    }

