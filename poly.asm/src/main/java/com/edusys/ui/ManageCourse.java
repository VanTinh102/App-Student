    void clear() {
        lblThematic2.setText("Thematic:");
        txtThematic.setText("");
        txtCreated.setText("");
        txtNote.setText("");
        txtTime.setText("");
        txtTuition.setText("");
        txtCreator.setText("");
    }

    ;
    void edit() {
        Integer id = (Integer) tblListCourse.getValueAt(this.row, 0);
        Course model = dao.selectById(id);
        this.setForm(model);
        jTabbedPane1.setSelectedIndex(0);
        this.updateStatus();
    }

    ;
    void setForm(Course model) {
        cboThematic.setToolTipText(String.valueOf(model.getCourseCode()));
        cboThematic.setSelectedItem(tdao.selectById(model.getThematicCode()));
        txtOpening.setText(XDate.toString(model.getKhaigiang(), "mm/dd/yyyy"));
        txtTuition.setText(String.valueOf(model.getTuition()));
        txtTime.setText(String.valueOf(model.getTime()));
        txtCreator.setText(model.getStaffCode());
        txtCreated.setText(XDate.toString(model.getNgaytaoDate(), "mm/dd/yyyy"));
        txtNote.setText(model.getNote());

    }

    ;
    Course getForm() {
        Course model = new Course();
        Thematic thematic = (Thematic) cboThematic.getSelectedItem();
        model.setThematicCode(thematic.getThematicCode());
        model.setKhaigiang(XDate.toDate(txtOpening.getText(), "mm/dd/yyy"));
        model.setTuition(Double.valueOf(txtTuition.getText()));
        model.setTime(Integer.valueOf(txtTime.getText()));
        model.setNote(txtNote.getText());
        model.setStaffCode(Auth.userStaff.getStaffCode());
        model.setNgaytaoDate(XDate.toDate(txtCreated.getText(), "mm/dd/yyy"));
        model.setCourseCode(Integer.valueOf(cboThematic.getToolTipText()));
        return model;
    }
    public boolean check() {
    if (txtOpening.getText().equals("") || txtTuition.getText().equals("") || txtTime.getText().equals("")
                || txtCreated.getText().equals("") || txtCreator.getText().equals("") || txtNote.getText().equals("") ) {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập đủ dữ liệu sau đó ấn Save","Error",1);
            return false;
        }
    else if (!(txtTuition.getText()).matches("[0-9]{1,9}")) {
            JOptionPane.showMessageDialog(rootPane, "Học phí phải nhập số","Error",1);
            txtTuition.requestFocus();
            return false;
        }
       else if (!(txtTime.getText()).matches("[0-9]{1,8}")) {
            JOptionPane.showMessageDialog(rootPane, "Thời lượng phải nhập số","Error",1);
            txtTime.requestFocus();
            return false;
        }

        return true;
    }
