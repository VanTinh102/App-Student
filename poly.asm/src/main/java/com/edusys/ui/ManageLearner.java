    private void init() {
        this.setLocationRelativeTo(null);
        this.setIconImage(XImage.getAppIcon());
        new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date now = new Date();
                SimpleDateFormat dateFormat = new SimpleDateFormat("hh:mm:ss a");
                String text = dateFormat.format(now);
                lblClock.setText(text);
            }
        }).start();
        this.fillTable();
        
        this.updataStatus();
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblListLearner.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtSearch.getText();
            List<Learner> list = dao.selectByKeyword(keyword);
            for (Learner nh : list) {
                Object[] row = {
                    nh.getLearnerCode(),
                    nh.getName(),
                    nh.getSex() ? "Nam" : "Nữ",
                    XDate.toString(nh.getDateOfBirth(),"MM/dd/yyyy"),
                    nh.getPhone(),
                    nh.getEmail(),
                    nh.getStaffCode(),
                    nh.getRegistrationDate(),
                    nh.getNote()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }

    }

