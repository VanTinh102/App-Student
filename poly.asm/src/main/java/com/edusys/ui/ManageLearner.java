    void update() {
        Learner learner = getForm();
        try {
            dao.update(learner);
            this.fillTable();
            MsgBox.alert(this, MsgBox.msgupdate);
        } catch (Exception e) {
            MsgBox.alert(this, MsgBox.msgnotupdate);
        }
    }

    ;
    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "you do not have the right to delete employees");
        } else {
            String learnerCode = txtLearnerCode.getText();
            if (MsgBox.confirm(this, "Do you want to delete this employee?")) {
                try {
                    dao.delete(learnerCode);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "successful delete");
                } catch (Exception e) {
                    MsgBox.alert(this, "delete failed");
                }
            }
        }
    }

    ;
    void edit() {
       String learnerString = (String) tblListLearner.getValueAt(this.row,0);
        Learner model = dao.selectById(learnerString);
        this.setForm(model);
        jTabbedPane1.setSelectedIndex(0);
        this.updataStatus();
    }

    ;

 
    void updataStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblListLearner.getRowCount() - 1);
        //trang thai form
        txtLearnerCode.setEditable(!edit);
        btnAdd.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);
        //trang tthai dieu huong
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    };
    void seach(){
        this.fillTable();
        this.clearForm();
        this.row = -1;
        updataStatus();
    }
    boolean check(){
         String err = "Please do not leave it blank";
            boolean b = true;
        if (txtLearnerCode.getText().equals("")) {
            lblErrLearnerCode.setText(err);
            b = false;
        } else {
            lblErrLearnerCode.setText("");
            b = true;
        }
        if (txtLearnerName.getText().equals("")) {
            lblErrName.setText(err);
            b = false;
        } else {
            lblErrName.setText("");
            b = true;
        }
        if (txtDateOfBirth.getText().equals("")) {
            lblErrDateOfBirth.setText(err);
            b = false;
        } else {
            lblErrDateOfBirth.setText("");
            b = true;
        }
          if (!rdoMale.isSelected() && !rdoFemale.isSelected()) {
            lblErrSex.setText("you have not entered the account role");
            b = false;
        } else {
            lblErrSex.setText("");
            b = true;
        }
           return b;
    }
   
    
