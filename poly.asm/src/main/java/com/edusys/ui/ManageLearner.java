    void setForm(Learner learner) {
        txtLearnerCode.setText(learner.getLearnerCode());
        txtLearnerName.setText(learner.getName());
        rdoMale.setSelected(learner.getSex());
        rdoFemale.setSelected(!learner.getSex());
        txtDateOfBirth.setText(XDate.toString(learner.getDateOfBirth(), "MM/dd/yyyy"));
        txtPhone.setText(learner.getPhone());
        txtEmail.setText(learner.getEmail());
        txtNote.setText(learner.getNote());
    }

