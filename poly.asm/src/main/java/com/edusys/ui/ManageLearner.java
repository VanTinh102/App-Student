    void insert() {
        Learner learner = getForm();
        try {
            dao.insert(learner);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, MsgBox.msginsert);
        } catch (Exception e) {
           MsgBox.alert(this, MsgBox.msgnotinsert);

                
        }

    }
 void first(){
        this.row = 0;
        this.edit();
    };
    void prev(){
        if(this.row>0){
            this.row--;
            this.edit();
        }
    };
    void next(){
        if(this.row<tblListLearner.getRowCount()-1){
            this.row ++;
            this.edit();
        }
    };
    void last(){
        this.row = tblListLearner.getRowCount()-1;
        this.edit();
    };
