    void clearForm() {
        txtLearnerCode.setText("");
        txtLearnerName.setText("");
        txtDateOfBirth.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
        txtNote.setText("");
        this.row = - 1;
        this.updataStatus();
    }

