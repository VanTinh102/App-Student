    Learner getForm() {
        Learner learner = new Learner();
        learner.setLearnerCode(txtLearnerCode.getText());
        learner.setName(txtLearnerName.getText());
        learner.setSex(rdoMale.isSelected());
        learner.setDateOfBirth(XDate.toDate(txtDateOfBirth.getText(),"MM/dd/yyyy"));
        learner.setPhone(txtPhone.getText());
        learner.setEmail(txtEmail.getText());
        learner.setNote(txtNote.getText());
        learner.setStaffCode(Auth.userStaff.getStaffCode());
        learner.setRegistrationDate(XDate.toString(new Date(), "MM/dd/yyyy"));
        return learner;
    }

