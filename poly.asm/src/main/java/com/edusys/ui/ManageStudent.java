    void fillComboBoxThematic(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboThematic.getModel();
        model.removeAllElements();
        List<Thematic> list = tdao.selectAll();
        for(Thematic cd : list){
            model.addElement(cd);
          
        }
       fillComboBoxCourse();
    };
    void fillComboBoxCourse(){
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboCourse.getModel();
        model.removeAllElements();
        Thematic thematic = (Thematic) cboThematic.getSelectedItem();
        if(thematic!=null){
            List<Course> list = cdao.selectByThematic(thematic.getThematicCode());
            for(Course t : list){
                model.addElement(t); 
                
            }
            fillTable();
        }
    };
    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
        model.setRowCount(0);
        Course course = (Course) cboCourse.getSelectedItem();       
        if(course!= null){
            List<Student> list = sdao.selectByCourse(course.getCourseCode());
            for(int i = 0;i<list.size();i++){
                Student hv = list.get(i);
                String Name = ldao.selectById(hv.getLearnerCode()).getName();
                model.addRow(new Object[]{i+1,hv.getStudentCode(),hv.getLearnerCode(),Name,hv.getPoint()});
            }
            
           fillLearner();
        }
    };
