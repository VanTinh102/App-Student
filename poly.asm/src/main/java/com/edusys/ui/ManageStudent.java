    void fillLearner(){
        DefaultTableModel model = (DefaultTableModel) tblLearner.getModel();
        model.setRowCount(0);
        try {
            Course khoaHoc = (Course) cboCourse.getSelectedItem();
            String keyword = txtSearch.getText();
            List<Learner> list = ldao.selectNotInCourse(khoaHoc.getCourseCode(), keyword);
            for (Learner nh : list) {
                model.addRow(new Object[]{
                    nh.getLearnerCode(),
                    nh.getName(),
                    nh.getSex()? "Nam" : "Nữ",
                    nh.getDateOfBirth(),
                    nh.getPhone(),
                    nh.getEmail()
                });
            }
        } catch (Exception e) {
        }
    }
    void removeStudent(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không được xóa học viên");
        }else{
            if(MsgBox.confirm(this, "Bạn muốn xóa các học viên được chọn?"));{
            for(int row: tblStudent.getSelectedRows()){
                int studentId = (int) tblStudent.getValueAt(row, 0);
                sdao.delete(studentId);
            }
            fillTable();
        }
        }
    };
    void addStudent(){
        
        Course course = (Course) cboCourse.getSelectedItem();
       
        int[] rows = tblLearner.getSelectedRows();
        
             for(int row : rows){
            String manh = (String) tblLearner.getValueAt(row, 0);
            Student hv = new Student();
            hv.setCourseCode(course.getCourseCode());
            hv.setPoint(0);
            hv.setLearnerCode(manh);
            sdao.insert(hv);
            
        }
        fillTable();
        jTabbedPane1.setSelectedIndex(0);
        
       
    };
    void updatePoint(){
        for(int i = 0;i < tblStudent.getRowCount();i++){
            int studentId = (int) tblStudent.getValueAt(i, 1);
            Student hv = sdao.selectById(studentId);
            hv.setPoint((float) tblStudent.getValueAt(i, 4));
            sdao.update(hv);
        }
        MsgBox.alert(this, MsgBox.msgupdate);
    };  
    
